package ourLib.dbUtils;


import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.util.LinkedList;

import ourLib.AppException;

			
public abstract class Dao<ENTITY>{
	
	private PreparedStatement st=null;
	private ResultSet rs=null;
	private Connection con=null;
	
	abstract protected ENTITY mapFromResulset(ResultSet rs) throws SQLException, AppException;
	abstract public ENTITY getOne(ENTITY p) throws AppException;
	abstract public LinkedList<ENTITY> getAll() throws AppException;
	abstract public void add(ENTITY p) throws AppException;
	abstract public void update(ENTITY p) throws AppException;
	abstract public void delete(ENTITY p) throws AppException;
	
	
	protected void closeResourses(AutoCloseable ...resources) throws AppException{
		try {
			for(AutoCloseable r: resources) {
				if(r!=null) {r.close();}
			}
			DbConnector.getInstancia().releaseConn();
		} 
		catch (Exception e) { throw new AppException(e.getMessage(), 500); }
	}
	
	
	protected LinkedList<ENTITY> doFindAll(StatementWrapper stw) throws AppException {
		LinkedList<ENTITY> outputFound= new LinkedList<>();
		try {
			con=DbConnector.getInstancia().getConn();
			st=stw.makeStatement(con);
			
			rs= st.executeQuery();
			
			while(rs!=null && rs.next()) {
				outputFound.add(mapFromResulset(rs));
			}	
		}
		catch (SQLException e) { throw new AppException(e.getMessage(), 500); } 
		finally { closeResourses(st,rs); }
		
		return outputFound;
	}
	

	protected ENTITY doGetOne(StatementWrapper stw) throws AppException {
		ENTITY outputFound=null;
		try {
			con=DbConnector.getInstancia().getConn();
			
			st=stw.makeStatement(con);
		
			rs=st.executeQuery();
		
			if(rs!=null && rs.next()) {
				outputFound=mapFromResulset(rs);
			}
		} 
		catch (SQLException e) { throw new AppException(e.getMessage(), 500); } 
		finally { closeResourses(st,rs); }
		return outputFound;//DEBERIA RETORNAR NULL????
	}
	
	
	protected void doModification(StatementWrapper stw) throws AppException {
		try {
			con=DbConnector.getInstancia().getConn();
			st=stw.makeStatement(con);
			st.executeUpdate();
		} 
		catch (SQLException e) { throw new AppException(e.getMessage(), 500); } 
		finally { closeResourses(st); }
	}
	
	
	protected void doAddWithGeneratedKeys(StatementWrapper stw, HasAutogeneratedKey ent) throws AppException {
		try {
			Connection con=DbConnector.getInstancia().getConn();
			PreparedStatement st= stw.makeStatement(con, PreparedStatement.RETURN_GENERATED_KEYS);
			st.executeUpdate();
			
			rs=st.getGeneratedKeys();
            if(rs!=null && rs.next()){
               ent.SetAutogeneratedKey(rs);
            }	   
		}  
		catch (SQLException e) { throw new AppException(e.getMessage(), 500); } 
		finally { closeResourses(st, rs); }
    }


	

}
